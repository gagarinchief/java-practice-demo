package Week2;

public class LoopsExercises {
    public static void main(String[] args) {
        for (int i = 1; i <= 10 ; i++) {
            System.out.println(i);
        }
        int a = 0;
        String s = "a";
        while (a <= 150) {
            a++;
            System.out.print(s);
        }

        for (int i = 0; i <=150 ; i++) {
            System.out.print(s);
        }

        for (int i = 10; i >= 1 ; i--) {
            System.out.println(i + " ");
        }
    }
}
